// --- Static data this client-safe module needs ---
// Both files are small (a few KB). The heavy scraped quote dataset and the
// generateQuotes() engine live in @/lib/quotes-engine (server-only) so that
// importing anything here never bundles ~5 MB of quote JSON into client code.
//
// provider-names.json: slug -> display name for providers discovered only in
// scraped data. Pre-generated by scripts/build-provider-names.ts (run in the
// scrape pipeline). Was previously derived at runtime from the full dataset.
// trustpilot-ratings.json: the live Trustpilot scores, overlaid onto the
// hardcoded providers below.
import providerNamesData from "@/data/provider-names.json";
import trustpilotRatingsData from "@/data/scraped/trustpilot-ratings.json";
import xeRatesData from "@/data/scraped/xe-midmarket-rates.json";

const providerNames = providerNamesData as Record<string, string>;
const discoveredSlugs = Object.keys(providerNames);

// --- Exchange rates from XE mid-market (authoritative), USD-based ---
// Sourced from the small (~6 KB) rates file directly, NOT from the quote
// dataset, so this stays client-safe (useExchangeRates imports exchangeRates).
const xeRates = xeRatesData as { rates: Record<string, number> };
export const exchangeRates: Record<string, number> = { USD: 1 };
if (xeRates.rates) {
  for (const [code, rate] of Object.entries(xeRates.rates)) {
    exchangeRates[code] = rate as number;
  }
}

export function getExchangeRate(from: string, to: string): number {
  const fromRate = exchangeRates[from] || 1;
  const toRate = exchangeRates[to] || 1;
  return toRate / fromRate;
}

// Map Trustpilot labels to our 4-value system
function toRatingLabel(score: number): Provider["ratingLabel"] {
  if (score >= 4.3) return "Excellent";
  if (score >= 3.5) return "Good";
  if (score >= 2.5) return "Fair";
  return "Poor";
}

// Overlay Trustpilot ratings onto hardcoded providers at module load, so every
// consumer (client and server) sees live ratings. Uses the small ratings file
// directly — not the quote dataset.
const trustpilotBySlug: Record<string, { score: number | null }> = {};
for (const r of trustpilotRatingsData as { slug: string; score: number | null }[]) {
  trustpilotBySlug[r.slug] = r;
}
for (const provider of providers) {
  const tp = trustpilotBySlug[provider.slug];
  if (tp && tp.score !== null) {
    provider.rating = tp.score;
    provider.ratingLabel = toRatingLabel(tp.score);
  }
}

export function getProvider(slug: string): Provider | undefined {
  return providers.find((p) => p.slug === slug);
}

/** All provider slugs we have data for (hardcoded + scraped sources like Monito) */
export function getAllProviderSlugs(): string[] {
  const slugs = new Set<string>(providers.map((p) => p.slug));
  for (const s of discoveredSlugs) slugs.add(s);
  return [...slugs].sort();
}

/** Get display name for any provider slug (hardcoded or discovered) */
export function getProviderName(slug: string): string {
  const provider = providers.find((p) => p.slug === slug);
  if (provider) return provider.name;
  return providerNames[slug] || slug.replace(/-/g, " ").replace(/\b\w/g, (c) => c.toUpperCase());
}

