export interface Provider {
  slug: string;
  name: string;
  logo: string;
  rating: number;
  ratingLabel: "Excellent" | "Good" | "Fair" | "Poor";
  description: string;
  founded: number;
  headquarters: string;
  regulated: boolean;
  regulators: string[];
  website: string;
  minTransfer: number;
  maxTransfer: number | null;
  transferSpeed: string;
  supportedCountries: number;
  supportedCurrencies: number;
  paymentMethods: string[];
  deliveryMethods: string[];
  pros: string[];
  cons: string[];
  features: string[];
  feeStructure: string;
  exchangeRateMarkup: string;
}

export interface TransferQuote {
  providerSlug: string;
  sendAmount: number;
  sendCurrency: string;
  receiveCurrency: string;
  exchangeRate: number;
  fee: number;
  receiveAmount: number;
  transferSpeed: string;
  rating: number;
  ratingLabel: "Excellent" | "Good" | "Fair" | "Poor";
  // Partner-direct brokers (e.g. Regency FX) don't expose a public rate feed.
  // We surface the mid-market rate as an "Indicative Rate" with no fee shown
  // and route the user to the partner for an actual quote — same pattern
  // RemitFinder and other comparison sites use for account-managed FX brokers.
  isIndicative?: boolean;
  /** Informational promo note (e.g. a better first-transfer rate). The
   * comparison rate stays standard; this is surfaced as a small badge. */
  promoNote?: string;
}

